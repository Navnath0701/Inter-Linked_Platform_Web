            <h2 class="about-sub-headings">History</h2>
            <p class="history-text">The beginning of formal technical education in India can be dated back to the mid-19th century. Major policy initiatives in the pre-independence period included the appointment of the <br> Indian  Universities Commission in 1902, issue of the Indian Education Policy Resolution in 1904, and the Governor Generals policy statement of 1913 stressing the importance of technical education,<br> the establishment of IISc in Bangalore, Institute for Sugar, Textile & Leather Technology in Kanpur, N.C.E. in Bengal in 1905, and industrial schools in several provinces.</p>
            <h2 class="about-sub-headings">The All India Council For Technical Education Act 1987</h2>
            <p class="history-text"><br>(No 52 of 1987 as passed by both the Houses of Parliament)<br><br>
        
                The AICTE Act was constituted to provide for the establishment of an All India Council for Technical Education with a view to proper planning and co-ordinated development of a technical education system throughout the country, the promotion of qualitative improvements of such education in relation to planned quantitative growth, and regulation & proper maintenance of norms and standards in the technical education system and for the matters connected therewith.</p>
